package day7;

public class FunctionMethodClass {

    public int sum(int a,int b){
        int sum;
        sum = a + b;
        return sum;
    }

    public int sub(int a, int b){
        int sub;
        sub = a - b;
        return sub;
    }

    public double div(double a, double b){
        double div;
        div = a / b;
        return div;
    }

    public double mul(double a, double b){
        double mul;
        mul = a * b;
        return mul;
    }

    public double per(double a, double b){
        double per;
        per = a % b;
        return per;
    }

}
